create type "public"."role" as enum ('admin', 'staff', 'sponsor', 'speaker', 'viewer', 'attendee');

create table "public"."user_roles" (
    "user_id" uuid not null,
    "role" role not null
);


alter table "public"."user_roles" enable row level security;

create table "public"."users" (
    "id" uuid not null,
    "created_at" timestamp without time zone not null default now()
);


alter table "public"."users" enable row level security;

CREATE UNIQUE INDEX user_roles_pkey ON public.user_roles USING btree (user_id, role);

CREATE UNIQUE INDEX users_pkey ON public.users USING btree (id);

alter table "public"."user_roles" add constraint "user_roles_pkey" PRIMARY KEY using index "user_roles_pkey";

alter table "public"."users" add constraint "users_pkey" PRIMARY KEY using index "users_pkey";

alter table "public"."user_roles" add constraint "user_roles_user_id_fkey" FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE not valid;

alter table "public"."user_roles" validate constraint "user_roles_user_id_fkey";

alter table "public"."users" add constraint "users_id_fkey" FOREIGN KEY (id) REFERENCES auth.users(id) ON DELETE CASCADE not valid;

alter table "public"."users" validate constraint "users_id_fkey";

